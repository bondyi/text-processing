namespace TextProcessing
{
    public enum TypeSymbol : byte
    {
        Unknown,
        Standart,           //обычный символ
        WhiteSpace,         //пробел
        Point,              //точка
        Comma,              //запятая
        Dash,               //тире
        Colon,              //двоеточие
        Semicolon,          //точка с запятой
        ExclamationPoint,   //восклицательный знак
        QuestionPoint,      //вопросительный знак
    }
}
